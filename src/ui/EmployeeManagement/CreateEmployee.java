    public CreateEmployee() {
        initComponents();
        // Suggest next Employee ID based on DB (max numeric id + 1). If non-numeric IDs exist, leave blank.
        try {
            EmployeeDAO dao = new EmployeeDAO();
            List<Employee> all = dao.findAll();
            int max = -1;
            for (Employee e : all) {
                try {
                    int val = Integer.parseInt(e.getEmployeeId());
                    if (val > max) max = val;
                } catch (NumberFormatException ignore) {}
            }
            if (max >= 0) {
                jTextFieldEmpNum.setText(String.valueOf(max + 1));
            }
        } catch (Exception ex) {
            // Non-fatal: allow manual entry
        }
    }

