    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // Validate required fields
        if (jTextFieldFirstName.getText().trim().isEmpty() ||
            jTextFieldLastName.getText().trim().isEmpty() ||
            jTextFieldAddress.getText().trim().isEmpty() ||
            jTextFieldPhoneNum.getText().trim().isEmpty() ||
            jTextFieldSSSNum.getText().trim().isEmpty() ||
            jTextFieldPhilHealthNum.getText().trim().isEmpty() ||
            jTextFieldTINNum.getText().trim().isEmpty() ||
            jTextFieldPagibigNum.getText().trim().isEmpty() ||
            jTextFieldBasicSalary.getText().trim().isEmpty() ||
            jTextFieldRiceSubsidy.getText().trim().isEmpty() ||
            jTextFieldPhoneAllowance.getText().trim().isEmpty() ||
            jTextFieldClothingAllowance.getText().trim().isEmpty() ||
            jTextFieldGrossSemiMonthly.getText().trim().isEmpty() ||
            jTextFieldHourlyRate.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(jButtonSave, "Please fill in all fields");
            return;
        }

        Validator validator = new Validator();
        String id = jTextFieldEmpNum.getText().trim();

        if (!validator.isValidString(jTextFieldLastName.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please input valid Last Name");
            return;
        }
        if (!validator.isValidString(jTextFieldFirstName.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please input valid First Name");
            return;
        }

        SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy");
        String birthday = sdf.format(jCalendarBirthday.getDate());

        if (!validator.isValidAddress(jTextFieldAddress.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please exclude using commas in Address");
            return;
        }
        if (!validator.isValidPhoneNumber(jTextFieldPhoneNum.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please input valid Phone Number (xxx-xxx-xxx)");
            return;
        }
        if (!validator.isValidSssNumber(jTextFieldSSSNum.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please format SSS as xx-xxxxxxx-x");
            return;
        }
        if (!validator.isValidPhilhealthNumber(jTextFieldPhilHealthNum.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please format PhilHealth as xxxxxxxxxxxx (12 digits)");
            return;
        }
        if (!validator.isValidTin(jTextFieldTINNum.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please format TIN as xxx-xxx-xxx-xxx");
            return;
        }
        if (!validator.isValidPagibigNumber(jTextFieldPagibigNum.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please format Pagibig as xxxxxxxxxxxx (12 digits)");
            return;
        }
        if (!validator.isValidNumber(jTextFieldBasicSalary.getText()) ||
            !validator.isValidNumber(jTextFieldRiceSubsidy.getText()) ||
            !validator.isValidNumber(jTextFieldPhoneAllowance.getText()) ||
            !validator.isValidNumber(jTextFieldClothingAllowance.getText()) ||
            !validator.isValidNumber(jTextFieldGrossSemiMonthly.getText()) ||
            !validator.isValidNumber(jTextFieldHourlyRate.getText())) {
            JOptionPane.showMessageDialog(jButtonSave, "Please input valid numeric values for salary fields");
            return;
        }

        String status = (String) jComboBoxStatus.getSelectedItem();
        String position = (String) jComboBoxPosition.getSelectedItem();
        String supervisor = (String) jComboBoxSupervisor.getSelectedItem();

        // If ID is empty, generate a simple next ID based on time
        if (id.isEmpty()) {
            id = String.valueOf(System.currentTimeMillis() % 100000000);
        }

        try {
            Employee.EmploymentStatus statusEnum;
            try {
                statusEnum = Employee.EmploymentStatus.valueOf(status);
            } catch (Exception ignore) {
                statusEnum = Employee.EmploymentStatus.PROBATIONARY;
            }

            Date birthdayRaw = jCalendarBirthday.getDate();
            if (birthdayRaw == null) {
                throw new IllegalArgumentException("Birthday is required");
            }
            LocalDate birthdayDate = birthdayRaw.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            BigDecimal basicSalary = new BigDecimal(jTextFieldBasicSalary.getText().trim());
            BigDecimal riceSubsidy = new BigDecimal(jTextFieldRiceSubsidy.getText().trim());
            BigDecimal phoneAllowance = new BigDecimal(jTextFieldPhoneAllowance.getText().trim());
            BigDecimal clothingAllowance = new BigDecimal(jTextFieldClothingAllowance.getText().trim());
            BigDecimal grossSemiMonthly = new BigDecimal(jTextFieldGrossSemiMonthly.getText().trim());
            BigDecimal hourlyRate = new BigDecimal(jTextFieldHourlyRate.getText().trim());

            Employee e = new Employee.Builder(
                    id,
                    jTextFieldLastName.getText(),
                    jTextFieldFirstName.getText(),
                    birthdayDate
            )
                    .withAddress(jTextFieldAddress.getText())
                    .withPhoneNumber(jTextFieldPhoneNum.getText())
                    .withSssNumber(jTextFieldSSSNum.getText())
                    .withPhilhealthNumber(jTextFieldPhilHealthNum.getText())
                    .withTinNumber(jTextFieldTINNum.getText())
                    .withPagIbigNumber(jTextFieldPagibigNum.getText())
                    .withStatus(statusEnum)
                    .withPosition(position)
                    .withImmediateSupervisor(supervisor)
                    .withBasicSalary(basicSalary)
                    .withRiceSubsidy(riceSubsidy)
                    .withPhoneAllowance(phoneAllowance)
                    .withClothingAllowance(clothingAllowance)
                    .withGrossSemiMonthlyRate(grossSemiMonthly)
                    .withHourlyRate(hourlyRate)
                    .build();

            EmployeeDAO dao = new EmployeeDAO();
            boolean ok = dao.create(e);
            if (ok) {
                JOptionPane.showMessageDialog(jButtonSave, "Employee created.");
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(jButtonSave, "Employee not created.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(jButtonSave, "Database error: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

